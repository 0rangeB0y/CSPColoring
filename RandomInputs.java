    private static Map<String, Object> getRandomInputs(int[][] adjacencyMatrix, String[] states) {
        int n = states.length;
        // Generate a list of indices and shuffle them to create a random permutation
        List<Integer> indices = IntStream.range(0, n).boxed().collect(Collectors.toList());
        Collections.shuffle(indices);

        // Permute the state names
        String[] permutedStates = new String[n];
        for (int i = 0; i < n; i++) {
            permutedStates[i] = states[indices.get(i)];
        }

        // Permute the adjacency matrix according to the shuffled indices
        int[][] permutedAdjacencyMatrix = new int[n][n];
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                permutedAdjacencyMatrix[i][j] = adjacencyMatrix[indices.get(i)][indices.get(j)];
            }
        }

        // Prepare the result as a map
        Map<String, Object> result = new HashMap<>();
        result.put("adjacencyMatrix", permutedAdjacencyMatrix);
        result.put("states", permutedStates);
        return result;
    }


    public static Map<String, Object> getRandomInputsAustralia()
    {
        return getRandomInputs(australiaAdjacencyMatrix, australiaStates);
    }

    public static Map<String, Object> getRandomInputsUS()
    {
        return getRandomInputs(usAdjacencyMatrix, usStates);
    }

